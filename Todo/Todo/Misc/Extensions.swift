                       animations: { self.alpha = 0 },
                       completion: { (value: Bool) in
                        self.isHidden = true
                        if let complete = onCompletion { complete() }
                       }
        )
    }
    
}
extension Date {
    static func -(recent: Date, previous: Date) -> (month: Int?, day: Int?, hour: Int?, minute: Int?, second: Int?) {
        let day = Calendar.current.dateComponents([.day], from: previous, to: recent).day
        let month = Calendar.current.dateComponents([.month], from: previous, to: recent).month
        let hour = Calendar.current.dateComponents([.hour], from: previous, to: recent).hour
        let minute = Calendar.current.dateComponents([.minute], from: previous, to: recent).minute
        let second = Calendar.current.dateComponents([.second], from: previous, to: recent).second
        
        return (month: month, day: day, hour: hour, minute: minute, second: second)
    }
    func checkIfWeekday(date: Date) -> Bool{
        let today = date
        let calendar = Calendar(identifier: .gregorian)
        let components = calendar.dateComponents([.weekday], from: today)
        switch components.weekday {
        case 1:
            return false
        case 2:
            return true
        case 3:
            return true
        case 4:
            return true
        case 5:
            return true
        case 6:
            return true
        case 7:
            return false
        default:
            return false
        }
    }
    func days(from date: Date) -> Int {
        return Calendar.current.dateComponents([.day], from: self, to: date).day ?? 0
    }
    func hours(from date: Date) -> Int {
        return Calendar.current.dateComponents([.hour], from: self, to: date).day ?? 0
    }
    func getDifference(date: String, task: Bool = false) -> String {
        let newFormatter = DateFormatter()
        newFormatter.dateFormat = "MMM dd,yyyy h:mm a"
        let newDate = newFormatter.date(from: date)!
        if (Calendar.current.isDateInYesterday(newDate)) {
            return "Yesterday"
        } else if (Calendar.current.isDateInToday(newDate)) {
            if !task {
                return "Due Today"
            } else {
                return "Today"
            }
        } else if (Calendar.current.isDateInTomorrow(newDate)) {
            if !task {
                return "Due Tomorrow"
            } else {
                return "Tomorrow"
            }
        } else {
            return date
        }
        
    }
    func getWeekDaysInEnglish() -> [String] {
       var calendar = Calendar(identifier: .gregorian)
       calendar.locale = Locale(identifier: "en_US_POSIX")
       return calendar.weekdaySymbols
     }

     enum Weekday: String {
       case monday, tuesday, wednesday, thursday, friday, saturday, sunday
     }

     enum SearchDirection {
       case next
       case previous

       var calendarSearchDirection: Calendar.SearchDirection {
         switch self {
         case .next:
           return .forward
         case .previous:
           return .backward
         }
       }
     }
    
    static func today() -> Date {
        return Date()
    }
    
    func next(_ weekday: Weekday, considerToday: Bool = false) -> Date {
        return get(.next,
                   weekday,
                   considerToday: considerToday)
    }
    
    func previous(_ weekday: Weekday, considerToday: Bool = false) -> Date {
        return get(.previous,
                   weekday,
                   considerToday: considerToday)
    }
    
    func get(_ direction: SearchDirection,
             _ weekDay: Weekday,
             considerToday consider: Bool = false) -> Date {
        
        let dayName = weekDay.rawValue
        
        let weekdaysName = getWeekDaysInEnglish().map { $0.lowercased() }
        
        assert(weekdaysName.contains(dayName), "weekday symbol should be in form \(weekdaysName)")
        
        let searchWeekdayIndex = weekdaysName.firstIndex(of: dayName)! + 1
        
        let calendar = Calendar(identifier: .gregorian)
        
        if consider && calendar.component(.weekday, from: self) == searchWeekdayIndex {
            return self
        }
        
        var nextDateComponent = calendar.dateComponents([.hour, .minute, .second], from: self)
        nextDateComponent.weekday = searchWeekdayIndex
        
        let date = calendar.nextDate(after: self,
                                     matching: nextDateComponent,
                                     matchingPolicy: .nextTime,
                                     direction: direction.calendarSearchDirection)
        
        return date!
    }
}
